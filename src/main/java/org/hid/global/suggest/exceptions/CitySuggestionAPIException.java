package org.hid.global.suggest.exceptions;

public class CitySuggestionAPIException extends Exception {

	private static final long serialVersionUID = 1L;

	public CitySuggestionAPIException() {
		super();
	}

	public CitySuggestionAPIException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public CitySuggestionAPIException(String message, Throwable cause) {
		super(message, cause);
	}

	public CitySuggestionAPIException(String message) {
		super(message);
	}

	public CitySuggestionAPIException(Throwable cause) {
		super(cause);
	}
	
	

}
